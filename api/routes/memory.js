const express = require('express');
const router = express.Router();
const multer = require('multer');

const storage = multer.diskStorage({
    destination: function(req, file, cb) {
        cb(null, './uploads/');
    },
    filename: function(req, file, cb) {
        cb(null, new Date().toISOString() + file.originalname);
    }
});

const fileFilter = (req, file, cb) => {
    if (file.mimetype === 'image/jpeg' || file.mimetype === 'image/png') {
     cb(null, true);   
    }else{
      cb( new Error('invalid file format'), false);  
    }      
};

const upload = multer({
    storage: storage, 
    limits: {
     fileSize: 1024 * 1024 * 5
},
    fileFilter: fileFilter
});

const Memory = require("../Models/memory");

router.post('/', upload.fields([{
    name: 'large_image', maxCount: 1},
    {
    name: 'small_image', maxCount: 1
    
}]), (req, res, next) => { 
    Memory.addMemory(req.body, function(err, result, fields) {
        if (err) {
            const response = {"_meta":{"status_code": 417, "message": err}};

            res.status(417);
            res.send(response);
        }else {
            const data = req.body;
            data.id = result.insertId;

            const response = {"_meta":{"status_code": 200}, "data": data};

            res.send(200);
            res.send(response);
        }
    });
});


router.put('/:memoryId', (req, res, next) => {
    Memory.updateMemory(req.params.memoryId, req.body, function(err, rows){
        if (err) {
            const response = {"_meta":{"status_code": 417, "message": err}};

            res.status(417);
            res.send(response);
        }else {
            const data = req.body;
            
            const response = {"_meta":{"status_code": 200}, "data": data};

            res.send(200);
            res.send(response);
        }
    });
});

router.delete('/:memoryId', (req, res, next) => {
    Memory.deleteMemory(req.params.memoryId, function(err, count) {

        if (err) {
            const response = {"_meta": {"status_code": 417, "message": err}};

            res.status(417);
            res.send(response);
        } else {

            const response = {"_meta":{"status_code": 200}};

            res.status(200);
            res.send(response);
        }
    });
});

module.exports = router;